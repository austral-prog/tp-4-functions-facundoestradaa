def number_to_month(month):
   """Given a number between 1-12, return the corresponding month"""
   if month == 1:
       return "enero"
   elif month == 2:
       return "febrero"
   elif month == 3:
       return "marzo"
   elif month == 4:
       return "abril"
   elif month == 5:
       return "mayo"
   if month == 6:
       return "junio"
   if month == 7:
       return "julio"
   if month == 8:
       return "agosto"
   if month == 9:
       return "septiembre"
   if month == 10:
       return "octubre"
   if month == 11:
       return "noviembre"
   if month == 12:
       return "diciembre"
   else:
       return "error"

